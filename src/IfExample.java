public class IfExample {
    public static void main(String[] args) {
        int money = 6500;

        if (money >= 8000) {
            System.out.println("순대국 마이쪙!");
        } else if (money >= 5000) {
            System.out.println("함박스테이끼 마이쪙~");
        } else {
            System.out.println("흑흑흑 배고프다 ㅠㅠ");
        }
    }
}
