public class SwitchCase {
    public static void main(String[] args) {
         int month = 5;

         switch (month) {
             case 1:
                 System.out.println("Jenuary");
                 break;
             case 2:
                 System.out.println("February");
                 break;
             case 3:
                 System.out.println("March");
                 break;
             case 4:
                 System.out.println("April");
                 break;
             default:
                 System.out.println("번역할 수 없음");
         }
    }
}
